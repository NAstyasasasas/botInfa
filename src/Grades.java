public class Grades {
    String studentsId;
    String nameCourses;
    double mark;
    String status;

    public Grades(String studentsId, String nameCourses, double mark, String status) {
        this.studentsId = studentsId;
        this.nameCourses = nameCourses;
        this.mark = mark;
        this.status = status;
    }

}
